class Solution {
    public static int countFractions(int n, int[] num, int[] den) {
        HashMap<Double, Integer> hmap = new HashMap<>();
        int count = 0;
        for(int i = 0; i < n; i++)
        {
            double key = (double)num[i] / (double) den[i];
            double key2 = (double)(den[i] - num[i]) / den[i];

            if(hmap.containsKey(key2))
                count += hmap.get(key2);
            hmap.put(key, hmap.getOrDefault(key, 0) +1);
        }
        
        return count;
    }
}
        
